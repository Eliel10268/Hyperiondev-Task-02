
#This will hold a phrase with exclamation marks
phrase= "The quick!brown!fox!jumps!over!the!lazy!dog!."

#this will replace exclamation marks by blank spaces
clear_phrase = phrase.replace("!"," ")

#this will display on the screen clear text without exclamation marks
print(clear_phrase)
#this will turn the letters into uppercase
Upper_phrase = clear_phrase.upper()

#this will display on the screen a text in capital letters/ uppercase
print(Upper_phrase)
#ths will display on the screen the text in reverse
print(Upper_phrase[::-1])